<script>
	import { gsap } from 'gsap'
	import { onMount, tick } from 'svelte'
	import { sheeldPathMapping } from './sheeld-path-mapping'

	export let animationDuration = 0.5
	export let isAnimating = false

	let pathRefs = []
	let svgRef

	// Массив индексов путей, которые должны менять цвет с #D9D9D9 на #CDEF33
	const colorChangePaths = [
		1, 2, 3, 5, 7, 9, 10, 11, 13, 15, 17, 18, 19, 21, 23, 25, 26, 27, 29, 31, 33, 34, 35, 37, 39,
		41, 42, 43, 45, 47, 49, 50, 51, 53, 55, 57, 58, 59, 61, 63, 65, 66, 67, 69, 71, 73, 74, 75, 77,
		79, 81, 82, 83, 85, 87, 89, 90, 91, 93, 95
	]

	let animations = []

	function startAnimations() {
		animations.forEach((anim) => anim.kill())
		animations = []

		pathRefs.forEach((path, index) => {
			if (path && sheeldPathMapping[index]) {
				// Animate path coordinates
				const anim = gsap.to(path, {
					duration: animationDuration,
					ease: 'power2.inOut',
					attr: { d: sheeldPathMapping[index].to }
				})
				animations.push(anim)

				// Animate color if this path should change color
				if (colorChangePaths.includes(index)) {
					const colorAnim = gsap.to(path, {
						duration: animationDuration,
						ease: 'power2.inOut',
						attr: { fill: '#CDEF33' }
					})
					animations.push(colorAnim)
				}
			}
		})
	}

	function stopAnimations() {
		animations.forEach((anim) => anim.kill())
		animations = []

		pathRefs.forEach((path, index) => {
			if (path && sheeldPathMapping[index]) {
				// Animate path coordinates
				const anim = gsap.to(path, {
					duration: animationDuration,
					ease: 'power2.inOut',
					attr: { d: sheeldPathMapping[index].from }
				})
				animations.push(anim)

				// Animate color if this path should change color
				if (colorChangePaths.includes(index)) {
					const colorAnim = gsap.to(path, {
						duration: animationDuration,
						ease: 'power2.inOut',
						attr: { fill: '#D9D9D9' }
					})
					animations.push(colorAnim)
				}
			}
		})
	}

	$: if (isAnimating) {
		startAnimations()
	} else {
		stopAnimations()
	}

	onMount(() => {
		pathRefs.forEach((path, index) => {
			if (path && sheeldPathMapping[index]) {
				path.setAttribute('d', sheeldPathMapping[index].from)
			}
		})
	})
</script>

